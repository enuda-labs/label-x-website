export const AnimatedButton: React.FC<AnimatedSectionProps & { onClick?: () => void }> = ({ 
  children, 
  className = '', 
  onClick 
}) => {
  return (
    <motion.button
      whileHover={{ scale: 1.05 }}
      whileTap={{ scale: 0.95 }}
      transition={{ duration: 0.2 }}
      onClick={onClick}
      className={className}
    >
      {children}
    </motion.button>
  );
};